package qedge.Nov4;

public class DemoLoop400 {

	public static void main(String[] args) {
for(int i=1;i<=5;i++)
		{
			System.out.println("ranga;;;;"+i);
		}
		System.out.println("=================");
for(int i=1;i<=5;i+=2)
		{
			System.out.println("ranga;;;;"+i);
		}
		System.out.println("=================");
for(int i=5;i>=1;i--)
		{
			System.out.println("ranga;;;;"+i);
		}
		
	}

}
