package qedge.Nov3;

public class FirstClass2 {

	public static void main(String[] args) {
		
		
		int i=100;
		i++;
		System.out.println(i);//101
		
		
		
		int j=900;
		j+=10;
		System.out.println(j);//910
		
		
		
		int x=700;
		x--;
		System.out.println(x);//699
		
		
		
		int y=300;
		y-=50;
		System.out.println(y);//250
		

	}

}
